def get_proc_mem():
    return psutil.Process(os.getpid()).memory_info().rss /1024**3


def get_GPU_mem():
    try:
        num = torch.cuda.device_count()
        mem = 0
        for i in range(num):
            mem_free, mem_total = torch.cuda.mem_get_info(i)
            mem += (mem_total - mem_free)/1024**3
        return mem
    except:
        return 0

def all_reduce_mean(x):
    world_size = get_world_size()
    if world_size > 1:
        x_reduce = torch.tensor(x).cuda()
        dist.all_reduce(x_reduce)
        x_reduce /= world_size
        return x_reduce.item()
    else:
        return x

def get_train_test_datasets(args, train_transform, val_transform):
    
    train_path = os.path.join(args.data_path, 'train')
    val_path = os.path.join(args.data_path, 'val')

    train_dataset = ImageFolder(train_path, transform=train_transform)
    val_dataset = ImageFolder(val_path, transform=val_transform)

    if args.frac_retained == 1.0:
        return train_dataset, val_dataset
    
    elif args.frac_retained < 1.0:
        # Get all possible classes in the folder
        classes = train_dataset.classes
        
        assert train_dataset.classes == val_dataset.classes, "Classes in train and val datasets must be the same"

        # Randomly select num_labels classes
        selected_classes = random.sample(classes, int(args.frac_retained * len(classes)))
        print("Number of classes retained: ", len(selected_classes))

        selected_class_indices = [train_dataset.class_to_idx[class_] for class_ in selected_classes]

        # Get the indices of the selected classes
        train_indices = [i for i, (_, index) in enumerate(train_dataset.imgs) if index in selected_class_indices]
        val_indices = [i for i, (_, index) in enumerate(val_dataset.imgs) if index in selected_class_indices]

        # Subset the datasets
        train_dataset = torch.utils.data.Subset(train_dataset, train_indices)
        val_dataset = torch.utils.data.Subset(val_dataset, val_indices)

        return train_dataset, val_dataset

    else:
        raise ValueError("Frac retained must be between 0 and 1")
